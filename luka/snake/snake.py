import pygame
import random
import time
from Jablko import Jablko
from warz import warz
from Dieuction import Dirction

#szerokość i wysokość ekranu
SZEROKOSC_EKRANU = 800
WYSOKOSC_EKRANU = 608

#stworzenie tla
tlo = pygame.Surface((800, 608))
for i in range(25):
    for j in range(19):
        obraz = pygame.image.load("images/background.png")
        maska = (random.randrange(0, 20), random.randrange(0,20), random.randrange(0,20))
       
        obraz.fill(maska, special_flags=pygame.BLEND_ADD)
        tlo.blit(obraz, (i*32, j*32))

#ustawienia
pygame.init()
#obiekt ekranu i zegara
ekran = pygame.display.set_mode([SZEROKOSC_EKRANU, WYSOKOSC_EKRANU])
zegar = pygame.time.Clock()
snake_P = warz()
porysz_wenzem = pygame.USEREVENT + 1
pygame.time.set_timer(porysz_wenzem, 200)

#jabłka
jablko = Jablko()
jablka = pygame.sprite.Group()
jablka.add(jablko)

gra_dziala = True
while gra_dziala:
    for event in pygame.event.get():
        if event.type == pygame.KEYDOWN:
            if event.key == pygame.K_ESCAPE:
                gra_dziala = False
            if event.key == pygame.K_w:
                warz.zmien_kierunek(Dirction.Gora)
            if event.key == pygame.K_s:
                warz.zmien_kierunek(Dirction.Dol)
            if event.key == pygame.K_a:
                warz.zmien_kierunek(Dirction.Lewo)
            if event.key == pygame.K_d:
                warz.zmien_kierunek(Dirction.Prawo)
        elif event.type == porysz_wenzem:
            warz.aktualizuj()
        elif event.type == pygame.QUIT:
            gra_dziala = False

    #rysowanie tla
    ekran.blit(tlo, (0, 0))
    ekran.blit(snake_P.obraz, snake_P.pozycja)
    #rysowanie jablek
    for jablko in jablka:
        ekran.blit(jablko.obraz, jablko.pozycja)
    
    #wyczyszczenie ekranu
    pygame.display.flip()
    #ustawienie stałego 30 FPS
    zegar.tick(30)

#opóxnienie 3 sekund
time.sleep(3)
#zamknięcie aplikacji
pygame.quit()




























































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































